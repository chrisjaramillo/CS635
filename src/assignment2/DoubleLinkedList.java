    @Override
    public boolean add(E e) {
        if(e == null)
        {
            return false;
        }
        if(count == 0)
        {
            first = last = new Node(e);
        }
        else
        {
            Node<E> newNode = new Node(e);
            first.add(newNode);
            if(first.previous() != null)
            {
                first = newNode;
            }
            else if(last.next() != null)
            {
                last = newNode;
            }
        }
        count++;
        return true;
    }

